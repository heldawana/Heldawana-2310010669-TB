    private void CariData(){
        try {
            st = kon.createStatement();
            String sql = "SELECT * FROM jenisizin WHERE nama LIKE '%"+tCari.getText()+"%'";
            rs = st.executeQuery(sql);
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("Kode");
            model.addColumn("Nama");
            
            
            int no = 1;   
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {                    
                    no++,
                    rs.getString("kode"),
                    rs.getString("nama")
                };    
                model.addRow(data);
                tbl1.setModel(model);
            }
        } catch (Exception e) {
        }
    }
            
