    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        // TODO add your handling code here:
        if (t1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon pilih data yang ingin dihapus terlebih dahulu");
        } else {
            int jawab = JOptionPane.showConfirmDialog(null, "Apakah yakin data jenis izin/cuti kode "+t1.getText()+" akan dihapus?","Konfirmasi",JOptionPane.YES_NO_OPTION);
            if (jawab==0) {
                try {
                    st = kon.createStatement();
                    String hapus = "DELETE FROM jenisizin WHERE kode='"+t1.getText()+"'";
                    st.execute(hapus);
                    JOptionPane.showMessageDialog(null, "Data jenis izin/cuti kode "+t1.getText()+" berhasil dihapus");
                    Bersih();
                    TampilData();                                                
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_bHapusActionPerformed

